#include "libft.h"

int	ft_maxint(int *tab)
{
	int i;
	int ret;

	i = -1;
	ret = tab[0];
	while (tab[++i] != -1)
		if (tab[i] > ret)
			ret = tab[i];
	return (ret);
}
